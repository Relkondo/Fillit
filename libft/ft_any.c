#include "libft.h"

int		ft_any(char **tab, int (*f)(char*))
{
	int	i;

	if (tab == 0 || !(f))
		return (0);
	i = 0;
	while (tab[i])
	{
		if (f(tab[i]))
			return (1);
		i++;
	}
	return (0);
}
